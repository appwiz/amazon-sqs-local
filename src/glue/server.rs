use std::sync::Arc;

use axum::body::Bytes;
use axum::extract::State;
use axum::http::HeaderMap;
use axum::response::IntoResponse;
use axum::routing::post;
use axum::{Json, Router};

use super::error::GlueError;
use super::state::GlueState;
use super::types::*;

macro_rules! dispatch {
    ($state:expr, $body:expr, $req_type:ty, $method:ident) => {{
        let req: $req_type = serde_json::from_slice(&$body)
            .map_err(|e| GlueError::ValidationException(e.to_string()))?;
        let resp = $state.$method(req).await?;
        Ok(Json(resp).into_response())
    }};
}

macro_rules! dispatch_empty {
    ($state:expr, $body:expr, $req_type:ty, $method:ident) => {{
        let req: $req_type = serde_json::from_slice(&$body)
            .map_err(|e| GlueError::ValidationException(e.to_string()))?;
        $state.$method(req).await?;
        Ok(Json(serde_json::json!({})).into_response())
    }};
}

async fn handle_request(
    State(state): State<Arc<GlueState>>,
    headers: HeaderMap,
    body: Bytes,
) -> Result<axum::response::Response, GlueError> {
    let target = headers
        .get("x-amz-target")
        .and_then(|v| v.to_str().ok())
        .ok_or_else(|| GlueError::InvalidAction("Missing X-Amz-Target header".into()))?;

    let action = target
        .strip_prefix("AWSGlue.")
        .ok_or_else(|| GlueError::InvalidAction(format!("Invalid target: {target}")))?;

    match action {
        "CreateDatabase" => dispatch!(state, body, CreateDatabaseRequest, create_database),
        "DescribeDatabase" => dispatch!(state, body, DescribeDatabaseRequest, describe_database),
        "ListDatabases" => dispatch!(state, body, ListDatabasesRequest, list_databases),
        "DeleteDatabase" => dispatch_empty!(state, body, DeleteDatabaseRequest, delete_database),
        "CreateTable" => dispatch!(state, body, CreateTableRequest, create_table),
        "DescribeTable" => dispatch!(state, body, DescribeTableRequest, describe_table),
        "ListTables" => dispatch!(state, body, ListTablesRequest, list_tables),
        "DeleteTable" => dispatch_empty!(state, body, DeleteTableRequest, delete_table),
        "CreateJob" => dispatch!(state, body, CreateJobRequest, create_job),
        "DescribeJob" => dispatch!(state, body, DescribeJobRequest, describe_job),
        "ListJobs" => dispatch!(state, body, ListJobsRequest, list_jobs),
        "DeleteJob" => dispatch_empty!(state, body, DeleteJobRequest, delete_job),
        _ => Err(GlueError::InvalidAction(format!("Unknown action: {action}"))),
    }
}

pub fn create_router(state: Arc<GlueState>) -> Router {
    Router::new()
        .route("/", post(handle_request))
        .with_state(state)
}


